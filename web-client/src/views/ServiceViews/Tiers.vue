          desto schlechter sind seine Bremswerte besonders auf nasser Fahrbahn, und auch die Traktion lässt deutlich nach. Besonders im Winter empfehlen sich
          Reifen mit möglichst viel Profil. In Österreich und der Schweiz gelten Winterreifen mit weniger als 4 Millimeter Profiltiefe übrigens als
