          3. Von O bis O - gilt das noch? Das hat der Autofahrer intus: Die Reifen werden zu Ostern und im Oktober gewechselt. Diese Faustregel ist im Großen
          und Ganzen sinnvoll, denn es kann bereits im Oktober schneien. Mit Ostern ist das aber so eine Sache: Ostersonntag ist nämlich immer am Sonntag, der
